        <p>Новый экспериментальный рейс будет состыкован в Азербайджане с рейсом Баку – Нью-Йорк – Баку.
            Рейсы будут выполняться ЗАО «Азербайджанские авиалинии» (AZAL) два раза в неделю – по
            вторникам и воскресеньям.</p>

        <p>случае подтверждения экономической целесообразности продолжения рейса, в дальнейшем,
            AZAL планирует состыковывать рейс Баку – Бишкек – Баку в Азербайджан с рейсами в Берлин
            или Франкфурт-на-Майне.</p>

        <p>Планируемый рейс по маршруту Бишкек – Баку – Нью-Йорк будет иметь неоценимо важное
            значение не только в транспортном и логистическом плане, но и способствовать развитию
            торгово-экономического и культурно-гуманитарного сотрудничества, отметили в МИД Кыргызстана.
